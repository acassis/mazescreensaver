void ico_renderer_init(IcoRenderer *ir) {
    GLuint vbo;
    glGenVertexArrays(1, &ir->vao);
    glGenBuffers(1, &vbo);

    glBindVertexArray(ir->vao);

    glBindBuffer(GL_ARRAY_BUFFER, vbo);
    glBufferData(GL_ARRAY_BUFFER, sizeof(ICO_VERTS),
                 ICO_VERTS, GL_STATIC_DRAW);

    /* aPos = 0 */
    glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE,
                          6 * sizeof(float), (void *)0);
    glEnableVertexAttribArray(0);
    /* aNor = 2 */
    glVertexAttribPointer(2, 3, GL_FLOAT, GL_FALSE,
                          6 * sizeof(float), (void *)(3 * sizeof(float)));
    glEnableVertexAttribArray(2);

    glBindVertexArray(0);
}

void ico_renderer_setup(const IcoRenderer *ir, const Shader *s) {
    glBindVertexArray(ir->vao);
    shader_set_bool(s, "rat",    0);
    shader_set_bool(s, "shaded", 1);
}

void ico_renderer_draw(const IcoRenderer *ir, const Shader *s,
                       const Ico *ico, float t) {
    Mat4 model = mat4_mul(
        mat4_mul(mat4_translate(ico->pos),
                 mat4_rotate_axis(ico->axis, DEG2RAD(t * 100.0f))),
        mat4_scale_f(0.25f));

    shader_set_mat4(s, "model", &model);
    glDrawArrays(GL_TRIANGLES, 0, 60);
    (void)ir;
}
